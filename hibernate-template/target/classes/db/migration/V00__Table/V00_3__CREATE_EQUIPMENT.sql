CREATE TABLE EQUIPMENT
(
	EQUIPMENT_OID		number(10) NOT NULL,
	EMPLOYEE_OID		number(10) NOT NULL,
	EQUIPMENT_NAME		varchar2(50) NOT NULL,
	EQUIPMENT_TYPE      varchar2(50) NOT NULL,
	-- audit fields --
	VERSION			number(10),
	CREATE_DATE		timestamp(6) DEFAULT SYSDATE,
	CREATE_USER		number(10) DEFAULT 1,
	UPDATE_DATE		timestamp(6) DEFAULT SYSDATE,
	UPDATE_USER		number(10) DEFAULT 1,
	------------------
	-- each equipment can only have 1 owner , so we make composite key for it
	CONSTRAINT	PK_EQUIPMENT PRIMARY KEY (EQUIPMENT_OID, EMPLOYEE_OID),
	CONSTRAINT	FK1_EQUIPMENT FOREIGN KEY (EMPLOYEE_OID) REFERENCES EMPLOYEE(EMPLOYEE_OID),
	CONSTRAINT	UC_EQUIPMENT UNIQUE KEY (EQUIPMENT_OID)
);